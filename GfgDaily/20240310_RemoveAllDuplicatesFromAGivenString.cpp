//User function template for C++
class Solution{
public:
	string removeDuplicates(string str) {
	    // code here
	    string ans;
	    unordered_set<char> s;
	    for (auto x : str) {
	        if (s.find(x) == s.end()) {
	            s.insert(x);
	            ans.push_back(x);
	        }
	    }
	    return ans;
	}
};

